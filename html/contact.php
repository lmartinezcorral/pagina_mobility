	<!-- Contact form AJAX handler -->
	<script>
		document.addEventListener('DOMContentLoaded', function() {
			const form = document.getElementById('contact-form');
			if (form) {
				form.addEventListener('submit', function(e) {
					e.preventDefault();
					const formData = new FormData(form);
					const messageDiv = document.getElementById('contact-message');
					const btn = document.getElementById('contact-submit-btn');
					const originalText = btn.innerHTML;

					btn.disabled = true;
					btn.innerHTML = '<span><i class="fas fa-spinner fa-spin"></i> Enviando...</span>';
					messageDiv.textContent = '';
					messageDiv.className = 'mb-2 small';

					fetch('assets/include/contact-action.php', {
						method: 'POST',
						body: formData
					})
					.then(response => response.json())
					.then(data => {
						if (data.success) {
							messageDiv.textContent = data.message;
							messageDiv.className = 'mb-2 small text-success';
							form.reset();
						} else {
							messageDiv.textContent = data.message || 'Error al enviar. Intenta más tarde.';
							messageDiv.className = 'mb-2 small text-danger';
						}
					})
					.catch(function() {
						messageDiv.textContent = 'Error de conexión. Intenta más tarde o escríbenos a somosmobility@gmail.com';
						messageDiv.className = 'mb-2 small text-danger';
					})
					.finally(function() {
						btn.disabled = false;
						btn.innerHTML = originalText;
					});
				});
			}
		});
	</script>
